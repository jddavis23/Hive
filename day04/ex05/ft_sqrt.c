int	ft_sqrt(int nb)
{
	int i;
	int result;

	result = 0;
	i = 1;
	if (nb < 0)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0 && i <= 46340 && nb / i == i)
		{
			result = i;
		}
		++i;
	}
	return (result);
}

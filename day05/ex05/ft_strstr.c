char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int a;

	i = 0;
	while (str[i] != '\0')
	{
		a = 0;
		while (str[i + a] != '\0' && str[i + a] == to_find[a])
		{
			if (to_find[a + 1] == '\0')
				return (&str[i]);
			++a;
		}
		++i;
	}
	return (0);
}

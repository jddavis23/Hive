char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	if (dest && src)
	{
		while (src[i] != '\0')
		{
			dest[i] = src[i];
			++i;
		}
		dest[i] = '\0';
		return (dest);
	}
	else
		return (0);
}

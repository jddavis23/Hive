void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long int a;

	a = nb;
	if (a < 0)
	{
		ft_putchar('-');
		a *= -1;
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putchar((a % 10) + '0');
	}
	else
		ft_putchar((a % 10) + '0');
}
